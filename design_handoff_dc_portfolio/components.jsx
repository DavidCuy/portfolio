function Button({ variant = 'primary', size, children, icon, iconAfter, onClick, href, className = '' }) {
  const cls = `btn btn-${variant}${size === 'sm' ? ' btn-sm' : ''}${size === 'lg' ? ' btn-lg' : ''} ${className}`
  const inner = (
    <>
      {icon}
      {children}
      {iconAfter}
    </>
  )
  if (href) return <a className={cls} href={href} onClick={onClick}>{inner}</a>
  return <button className={cls} onClick={onClick}>{inner}</button>
}

